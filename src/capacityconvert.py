

def convert(stri, wordsize):
    parts = stri.split(")")
    res = ""
    for part in parts:
        if len(part) > 1:
            part = part[1:]
            subparts = part.split(",")
            x = eval(subparts[0])
            y = eval(subparts[1])
            onesection = y**(1/float(20))
            sectionsize = x*wordsize
            rate = 1/sectionsize
            stri = "("+str(rate)+","+str(y)+")"
            res = stri + res #invert order
    return res

